# 14. Gerenciamento de Ambientes Virtuais:

# a) Crie um novo ambiente virtual usando o virtualenv.
# No terminal, execute: virtualenv meu_ambiente_virtual

# b) Instale uma biblioteca externa em um ambiente virtual usando o pip.
# No terminal, estando no ambiente virtual, execute: pip install nome_da_biblioteca

# c) Liste todos os pacotes instalados em um ambiente virtual.
# No terminal, estando no ambiente virtual, execute: pip list

# d) Ative um ambiente virtual e execute um script Python dentro dele.
# No terminal, estando na pasta do ambiente virtual, execute: source bin/activate
# Em seguida, execute o script Python normalmente.

# e) Desative um ambiente virtual.
# No terminal, estando no ambiente virtual, execute: deactivate

